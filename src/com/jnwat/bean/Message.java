package com.jnwat.bean;

public class Message {
    private String  type;//消息类型
    private String timeLong;//消息接收时间的长短
    private String content;//内容
   
    //消息的类型定义
    public final static String NOTICE_MESSAGE ="通知消息";
    public final static String PROCESS_SCHEDULE ="流程进度";
    public final static String ANNOUNCEMENT_MESSAGE="公告消息";
    public final static String REMINDER  = "温馨提示";
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTimeLong() {
		return timeLong;
	}
	public void setTimeLong(String timeLong) {
		this.timeLong = timeLong;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}

	

    
}
